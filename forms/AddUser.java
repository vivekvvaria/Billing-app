    public void create(){
        try {
            if(jTextField1.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null,"Please enter the name.");
                jTextField1.requestFocus();
            }
            else if(Pattern.matches("[0-9]",jTextField1.getText())){
                JOptionPane.showMessageDialog(null,"Please enter only alphabets in the name.");
                jTextField1.requestFocus();
            }
            else if(jTextField2.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null,"Please enter the surname.");
                jTextField2.requestFocus();
            }
            else if(Pattern.matches("[0-9]",jTextField2.getText())){
                JOptionPane.showMessageDialog(null,"Please enter only alpabets in the surname.");
                jTextField2.requestFocus();
            }
            else if(jTextField3.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null,"Please enter the mobile number.");
                jTextField3.requestFocus();
            }
            else if(!Pattern.matches("[0-9]{10}",jTextField3.getText()))
            {
                JOptionPane.showMessageDialog(null,"Please enter only ten numbers..");
                jTextField3.setText("");
                jTextField3.requestFocus();
            }
            else if(jTextField4.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null,"Please enter the address.");
                jTextField4.requestFocus();
            }
            else if(jTextField5.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null,"Please enter the email-address.");
                jTextField5.requestFocus();
            }
            else if(!Pattern.matches("[a-zA-Z0-9]{1,}[@]{1}[a-z]{1,6}[.]{1}+[a-z]{3}",jTextField5.getText()))
            {
                JOptionPane.showMessageDialog(null,"Invalid email-address pattern.");
                jTextField5.setText("");
                jTextField5.requestFocus();
            }
            else if(jTextField6.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null,"Please enter username.");
                jTextField6.requestFocus();
            }
            else if(jPasswordField1.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null,"Please enter the password.");
                jPasswordField1.requestFocus();
            }
            else if(!jPasswordField1.getText().equals(jPasswordField2.getText()))
            {
                jLabel9.setText("Password not matched!!!");
                jPasswordField1.setText("");
                jPasswordField2.setText("");
                jPasswordField1.requestFocus();
            }
            else
            {
                Statement st=con.createStatement();

                st.executeUpdate("insert into users values('"+jTextField1.getText()+"','"+jTextField2.getText()+"','"+jTextField3.getText()+"','"+jTextField4.getText()+"','"+jTextField5.getText()+"','"+jTextField6.getText()+"','"+jPasswordField1.getText()+"')");
                JOptionPane.showMessageDialog(null,"Successfully added!!!");
                clear();
                jTextField1.requestFocus();
            }    
        }
        catch(SQLException e) {
            JOptionPane.showMessageDialog(null,e.toString(),"Error.",JOptionPane.ERROR_MESSAGE);                    
            clear();
            jTextField1.requestFocus();
        }        
    }
    
